def _load_jsonl(path: Path) -> list[dict]:
    """Load records from a .jsonl file (one JSON object per line)."""
    if not path.exists():
        return []
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def _classify_merged_edge(
    edge: dict, qname_to_file: dict[str, str]
) -> Optional[dict]:
    """Classify a single edge from the merged graph into an Intra-Type cross-file edge.

    Returns a cross-file edge dict if the edge qualifies as intra-type, else None.
    An Intra-Type edge connects two different files of the same type (e.g., py→py imports,
    js→js imports, java→java extends).

    Source file comes from edge["file"]; target file comes from qname_to_file[to_qname].

    Qualifies when:
    - from_file and to_file both resolve to real file paths
    - Both files are in the same layer
    - Both files have the same type
    - The edge kind is one of the import/call/extends kinds that indicate cross-file references
    """
    # Intra-Type kinds: edges that represent cross-file *structural* dependencies
    # at compile/link time (NOT runtime call graph).
    # An Intra-Type edge connects two different files of the same type through
    # explicit structural relationships: import declarations or inheritance.
    _INTRA_TYPE_KINDS = frozenset((
        # Import/dependency edges (from language plugins)
        "imports", "extends", "implements",
        "jsp_include", "cpp_include", "c_include",
        # Java-specific inheritance
        "java_extends", "java_implements",
    ))

    kind = edge.get("kind", "")

    # Source file: edges from Stage 4 store it in the "file" field
    from_file = edge.get("file", "")
    # Target file: resolve via qname -> file mapping from the node index
    to_qname = edge.get("to_qname", "")
    to_file = qname_to_file.get(to_qname, "")

    if not from_file or not to_file:
        return None

    if from_file == to_file:
        return None  # same-file edge (internal), not cross-file

    from_layer = get_layer_from_path(from_file)
    to_layer = get_layer_from_path(to_file)

    if from_layer != to_layer:
        return None

    from_type = get_file_type(from_file)
    to_type = get_file_type(to_file)

    if from_type != to_type:
        return None

    # Only include edges that represent cross-file references
    if kind not in _INTRA_TYPE_KINDS:
        return None

    return {
        "from_qname": edge.get("from_qname", ""),
        "to_qname": to_qname,
        "from_file": from_file,
        "to_file": to_file,
        "from_layer": from_layer,
        "to_layer": to_layer,
        "from_type": from_type,
        "to_type": to_type,
        "kind": kind,
        "category": "intra_type",
    }



