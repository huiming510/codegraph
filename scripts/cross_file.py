def _load_cross_file_plugins():
    """Lazy-load CrossFilePlugin implementations."""
    try:
        from plugins.cross_file import CrossFileRegistry
        return CrossFileRegistry
    except ImportError:
        return None


def produce_cross_file_edges(
    subgraphs: dict[str, tuple[list[dict], list[dict]]],
    all_symbols: list[dict],
    merged_elements: dict = None,
    ast_data: list[dict] = None,
) -> list[dict]:
    """Call all CrossFilePlugins to produce cross-file edges.

    Args:
        subgraphs: Dict of subgraph_name -> (nodes, edges)
        all_symbols: Flat list of all symbols across subgraphs
        merged_elements: Merged element dict from all subgraphs (from _reconstruct_elements)
        ast_data: AST data for plugins that need it (e.g., for parsing annotations)

    Returns:
        List of cross-file edge dicts
    """
    all_edges: list[dict] = []
    if merged_elements is None:
        merged_elements = {"templates": []}

    registry_cls = _load_cross_file_plugins()
    if registry_cls is None:
        return all_edges

    try:
        plugins = []
        for name in registry_cls.available_plugins():
            plugin = registry_cls.get(name)
            if plugin:
                plugins.append(plugin)
    except Exception:
        return all_edges

    # Build a GraphContext with the merged symbols, edges
    all_edges_list: list[dict] = []
    for sg_name, (nodes, edges) in subgraphs.items():
        all_edges_list.extend(edges)

    from plugins.common.base import GraphContext
    context = GraphContext(
        symbols=all_symbols,
        edges=all_edges_list,
        ui_data=merged_elements,
        ast_data=ast_data if ast_data is not None else [],
    )

    for plugin in plugins:
        try:
            edges = plugin.produce(context, None, merged_elements)
            if edges:
                # Convert CrossFileEdge objects to dicts if needed
                for edge in edges:
                    if hasattr(edge, "to_dict"):
                        edge = edge.to_dict()
                    all_edges.append(edge)
        except Exception as e:
            import warnings
            warnings.warn(f"CrossFilePlugin {plugin.name} failed: {e}")

    return all_edges


# ── Deduplication ─────────────────────────────────────────────────────────────

def deduplicate_cross_file_edges(edges: list[dict]) -> list[dict]:
    """Remove duplicate cross-file edges based on kind, from, and to identifiers.

    Supports both field naming conventions:
    - from_qname/to_qname (used by cross-file plugins like jsp_java)
    - from_node/to_node (used by make_cross_file_edge)
    """
    seen: set[tuple] = set()
    unique: list[dict] = []

    for edge in edges:
        # Normalize field names: support both qname and node variants
        from_id = edge.get("from_qname") or edge.get("from_node", "")
        to_id = edge.get("to_qname") or edge.get("to_node", "")
        key = (edge.get("kind"), from_id, to_id)
        if key not in seen:
            seen.add(key)
            unique.append(edge)

    return unique


def classify_edge_category(from_layer: str, to_layer: str, from_type: str, to_type: str) -> str:
    """Classify an edge into one of three categories:
    
    - intra_type: Same file type (e.g., Java→Java, JSP→JSP)
    - cross_type: Different file types, same layer (e.g., Java→XML in source layer)
    - cross_layer: Different layers (always cross-type, e.g., Java→JSP)
    
    Args:
        from_layer: Layer of source (e.g., "01_source", "02_template")
        to_layer: Layer of target
        from_type: File type of source (e.g., "java", "jsp")
        to_type: File type of target
    
    Returns:
        Edge category: "intra_type", "cross_type", or "cross_layer"
    """
    # Check layer relation first
    if from_layer != to_layer:
        return CrossFileEdgeCategory.CROSS_LAYER_EDGE
    
    # Same layer - check type relation
    if from_type == to_type:
        return CrossFileEdgeCategory.INTRA_TYPE_EDGE
    else:
        return CrossFileEdgeCategory.CROSS_TYPE_EDGE


# ── Total graph building ──────────────────────────────────────────────────────

def build_total_graph(
    subgraphs: dict[str, tuple[list[dict], list[dict]]],
    cross_file_edges: list[dict],
    *,
    filtered_subgraphs: Optional[dict[str, tuple[list[dict], list[dict]]]] = None,
) -> dict:
    """Merge all subgraphs and cross-file edges into the master graph.

    Args:
        subgraphs: Dict of subgraph_name -> (nodes, edges)
        cross_file_edges: Pre-classified cross-file edge dicts (from _classify_merged_edge
                     and CrossFilePlugins). Each must have a "category" field:
                     "intra_type", "cross_type", or "cross_layer".
        filtered_subgraphs: Optional override — if provided, use these (filtered) nodes/edges
                     instead of the unfiltered subgraphs. This enables post-pruning the
                     merged graph with cross-file edges before writing to disk.

    Returns:
        Master graph dict with nodes, edges, cross_file_edges, metadata
    """
    # Use filtered subgraphs if provided (e.g., after merged-level reachability pruning)
    use_subgraphs = filtered_subgraphs if filtered_subgraphs is not None else subgraphs

    all_nodes: list[dict] = []
    all_edges: list[dict] = []
    layer_counts: dict[str, dict] = {}

    for subgraph_name, (nodes, edges) in use_subgraphs.items():
        all_nodes.extend(nodes)
        all_edges.extend(edges)
        layer_counts[subgraph_name] = {
            "nodes": len(nodes),
            "edges": len(edges),
        }

    # Edges are already classified by category — just separate them
    intra_type: list[dict] = []
    cross_type: list[dict] = []
    cross_layer: list[dict] = []
    for edge in cross_file_edges:
        category = edge.get("category", "")
        if category == "cross_layer":
            cross_layer.append(edge)
        elif category == "cross_type":
            cross_type.append(edge)
        else:
            intra_type.append(edge)

    return {
        "nodes": all_nodes,
        "edges": all_edges,
        "cross_file_edges": cross_file_edges,
        "intra_type_cross_file_edges": intra_type,
        "cross_type_cross_file_edges": cross_type,
        "cross_layer_cross_file_edges": cross_layer,
        "metadata": {
            "total_nodes": len(all_nodes),
            "total_edges": len(all_edges),
            "total_cross_file_edges": len(cross_file_edges),
            "intra_type_count": len(intra_type),
            "cross_type_count": len(cross_type),
            "cross_layer_count": len(cross_layer),
            "subgraph_counts": layer_counts,
        },
    }


# ── Stage 6 main function ─────────────────────────────────────────────────────

def generate_cross_file_edges(
    src_root: Path,
    out_dir: Path,
    subgraph_dirs: Optional[dict[str, Path]] = None,
) -> dict:
    """Stage 6: Generate cross-file edges and build total graph.

    This is the main entry point for Stage 6 in the --all pipeline.

    Intra-Type edges (same file type, e.g., JS→JS imports) are generated in Stage 4
    by language plugins (build_import_edges, etc.) and written to the merged edges.jsonl.
    Stage 6 reads them directly and classifies them.

    Cross-Type and Cross-Layer edges are generated by CrossFilePlugins.

    Workflow:
    1. Recursively scan for all subgraph directories
    2. Read the merged edges.jsonl from the root output directory
    3. Extract Intra-Type edges directly from the merged graph (Stage 4 origin)
    4. Call CrossFilePlugins for Cross-Type and Cross-Layer edges
    5. Combine all edges and write outputs

    Args:
        src_root: Source project root (for context)
        out_dir: Output directory containing the role architecture subdirectories
        subgraph_dirs: Optional dict of category -> Path mapping (overrides scanning)

    Returns:
        Total graph dict
    """
    print(f"Stage 6: Cross-File Edge Generation...")

    # ── Step 1: Load all subgraphs ───────────────────────────────────────────
    subgraphs: dict[str, tuple[list[dict], list[dict]]] = {}
    ui_data_by_subgraph: dict[str, dict] = {}
    all_symbols: list[dict] = []

    if subgraph_dirs:
        for category, dir_path in subgraph_dirs.items():
            if dir_path.exists():
                nodes, edges = load_subgraph(dir_path)
                if nodes or edges:
                    subgraphs[category] = (nodes, edges)
                    ui_data_by_subgraph[category] = load_elements(dir_path)
                    for node in nodes:
                        sym = _node_to_symbol(node)
                        if sym.get("qualified_name"):
                            all_symbols.append(sym)
    else:
        # Flat output structure: each type has its own subdirectory directly under out_dir/
        # e.g. output/java/nodes.jsonl, output/yaml/nodes.jsonl
        for child in sorted(out_dir.iterdir()):
            if not child.is_dir():
                continue
            nodes_file = child / "nodes.jsonl"
            if nodes_file.exists():
                category = child.name
                nodes, edges = load_subgraph(child)
                if nodes or edges:
                    subgraphs[category] = (nodes, edges)
                    ui_data_by_subgraph[category] = load_elements(child)
                    for node in nodes:
                        sym = _node_to_symbol(node)
                        if sym.get("qualified_name"):
                            all_symbols.append(sym)

    print(f"  Loaded {len(subgraphs)} subgraphs, {len(all_symbols)} total symbols")

    # ── Step 2: Extract Intra-Type edges from the merged edges.jsonl ───────────
    # Build qname -> file index from the merged nodes.jsonl (written by Stage 5).
    # Source file for each edge comes from edge["file"]; target file is resolved
    # by looking up edge["to_qname"] in the qname index.
    qname_to_file: dict[str, str] = {}
    merged_nodes_file = out_dir / "nodes.jsonl"
    for node in _load_jsonl(merged_nodes_file):
        qn = node.get("qualified_name") or node.get("id", "")
        f = node.get("file", "")
        if qn and f:
            qname_to_file[qn] = f

    merged_edges_file = out_dir / "edges.jsonl"
    merged_edges: list[dict] = _load_jsonl(merged_edges_file)

    seen_intra: set[tuple] = set()
    intra_type_edges: list[dict] = []
    for edge in merged_edges:
        classified = _classify_merged_edge(edge, qname_to_file)
        if classified:
            key = (classified["from_qname"], classified["to_qname"], classified["kind"])
            if key not in seen_intra:
                seen_intra.add(key)
                intra_type_edges.append(classified)

    print(f"  Intra-Type edges (from Stage 4): {len(intra_type_edges)}")

    # ── Step 3: CrossFilePlugins generate Cross-Type and Cross-Layer edges ─────
    merged_elements: dict = {"templates": []}
    for sg_elements in ui_data_by_subgraph.values():
        for key, value in sg_elements.items():
            if key == "templates":
                merged_elements.setdefault(key, []).extend(value or [])
            else:
                merged_elements.setdefault(key, []).extend(value if isinstance(value, list) else [])

    plugin_edges = produce_cross_file_edges(subgraphs, all_symbols, merged_elements, ast_data=None)
    plugin_edges = deduplicate_cross_file_edges(plugin_edges)

    # Classify plugin edges (Cross-Type or Cross-Layer)
    # Build qname -> file mapping from symbols for edge classification
    qname_to_sym_file: dict[str, str] = {}
    for sym in all_symbols:
        qn = sym.get("qualified_name", "")
        f = sym.get("file", "")
        if qn and f:
            qname_to_sym_file[qn] = f

    cross_type_edges: list[dict] = []
    cross_layer_edges: list[dict] = []
    for edge in plugin_edges:
        from_qname = edge.get("from_qname", "")
        to_qname = edge.get("to_qname", "")

        # Resolve files: explicit field > symbol qname mapping > qname prefix extraction
        from_file = edge.get("from_file", "") or qname_to_sym_file.get(from_qname, "") or _extract_file_from_qname(from_qname)
        to_file = edge.get("to_file", "") or qname_to_sym_file.get(to_qname, "") or _extract_file_from_qname(to_qname)

        from_layer = edge.get("from_layer", "") or get_layer_from_path(from_file)
        to_layer = edge.get("to_layer", "") or get_layer_from_path(to_file)
        from_type = edge.get("from_type", "") or get_file_type(from_file) or _infer_type_from_qname(from_qname)
        to_type = edge.get("to_type", "") or get_file_type(to_file) or _infer_type_from_qname(to_qname)
        edge["category"] = classify_edge_category(from_layer, to_layer, from_type, to_type)

        if edge["category"] == "cross_layer":
            cross_layer_edges.append(edge)
        elif edge["category"] == "cross_type":
            cross_type_edges.append(edge)

    print(f"  Cross-Type edges (from CrossFilePlugins): {len(cross_type_edges)}")
    print(f"  Cross-Layer edges (from CrossFilePlugins): {len(cross_layer_edges)}")

    # ── Step 4: Combine all cross-file edges ───────────────────────────────────
    all_cross_file_edges = intra_type_edges + cross_type_edges + cross_layer_edges
    print(f"  Total cross-file edges: {len(all_cross_file_edges)}")

    # ── Step 4b: Reachability pruning on the merged graph ──────────────────────
    # Use the FULL merged edge set (intra-type + cross-file) so that nodes reachable
    # only through cross-layer/cross-type edges are NOT incorrectly removed.
    # This is the single authoritative pruning point for the total graph.
    all_merged_edges = merged_edges + all_cross_file_edges
    filtered_symbols, filtered_edges, prune_stats = prune_disconnected_components(
        all_symbols, all_merged_edges, verbose=True
    )
    filtered_qnames: set[str] = {s["qualified_name"] for s in filtered_symbols}

    # Reconstruct filtered subgraphs — only nodes that survived pruning
    filtered_subgraphs: dict[str, tuple[list[dict], list[dict]]] = {}
    for sg_name, (nodes, edges) in subgraphs.items():
        sg_nodes = [
            n for n in nodes
            if (n.get("qualified_name") or n.get("id", "")) in filtered_qnames
        ]
        sg_edges = [
            e for e in edges
            if (e.get("from_qname", "") in filtered_qnames
                or e.get("to_qname", "") in filtered_qnames)
        ]
        if sg_nodes:
            filtered_subgraphs[sg_name] = (sg_nodes, sg_edges)

    # Collect db:: virtual node identifiers from cross-file edges so they survive pruning.
    # sql_mapping edges have to_qname like "db::article.id" which won't be in the symbol table.
    for edge in all_cross_file_edges:
        to_qn = edge.get("to_qname", "")
        from_qn = edge.get("from_qname", "")
        if to_qn.startswith("db::") or from_qn.startswith("db::"):
            if to_qn:
                filtered_qnames.add(to_qn)
            if from_qn:
                filtered_qnames.add(from_qn)

    # Keep only cross-file edges whose both endpoints survived pruning
    all_cross_file_edges = [
        e for e in all_cross_file_edges
        if (e.get("from_qname", "") in filtered_qnames
            or e.get("from_file", "") in filtered_qnames)
        and (e.get("to_qname", "") in filtered_qnames
            or e.get("to_file", "") in filtered_qnames)
    ]

    print(f"  After merged-level pruning: {len(filtered_symbols)} nodes, "
          f"{len(filtered_edges)} edges, {len(all_cross_file_edges)} cross-file edges remain")

    # ── Step 5: Build and write outputs ──────────────────────────────────────
    total_graph = build_total_graph(
        subgraphs, all_cross_file_edges, filtered_subgraphs=filtered_subgraphs
    )

    cross_edges_file = out_dir / "cross_file_edges.json"
    cross_edges_file.write_text(
        json.dumps(all_cross_file_edges, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  Written: {cross_edges_file}")

    all_graph_file = out_dir / "all_graph.json"
    all_graph_file.write_text(
        json.dumps(total_graph, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  Written: {all_graph_file}")

    # Write filtered nodes and edges back to the root directory
    # so _build_viewer reads the clean (pruned) graph
    filtered_out_nodes = total_graph["nodes"]
    filtered_out_edges = total_graph["edges"]
    (out_dir / "nodes.jsonl").write_text(
        "\n".join(json.dumps(n, ensure_ascii=False) for n in filtered_out_nodes),
        encoding="utf-8")
    (out_dir / "edges.jsonl").write_text(
        "\n".join(json.dumps(e, ensure_ascii=False) for e in filtered_out_edges),
        encoding="utf-8")
    print(f"  Written: nodes.jsonl ({len(filtered_out_nodes)}), edges.jsonl ({len(filtered_out_edges)})")

    return total_graph


def generate_cross_file_edges_for_type(
    src_root: Path,
    out_dir: Path,
    file_type: str,
) -> dict:
    """Generate cross-file edges for a specific file type (--singlefile mode).

    This variant:
    - Loads only subgraphs related to the specified type
    - Generates cross-file edges involving that type
    - Writes the subgraph + related cross-file edges (not the full master graph)

    Args:
        src_root: Source project root
        out_dir: Output directory
        file_type: File type code (e.g., "java", "jsp", "yaml")

    Returns:
        Dict with subgraph data and related cross-file edges
    """
    print(f"Stage 6: Cross-File Edge Generation for type: {file_type}")

    from classify import get_role_for_file_type

    target_role = get_role_for_file_type(file_type)
    if target_role is None:
        print(f"  Unknown file type: {file_type}, skipping cross-file edge generation")
        return {"nodes": [], "edges": [], "cross_file_edges": []}

    layer, subcat = target_role
    category = f"{layer}/{subcat}"

    # Load the target subgraph (flat: output/{type}/)
    target_dir = out_dir / file_type
    target_nodes, target_edges = load_subgraph(target_dir)

    # Find related subgraphs in the flat output directory
    subgraphs: dict[str, tuple[list[dict], list[dict]]] = {category: (target_nodes, target_edges)}
    ui_data_by_subgraph: dict[str, dict] = {category: _reconstruct_elements(target_nodes)}
    all_symbols: list[dict] = [_node_to_symbol(n) for n in target_nodes if _node_to_symbol(n).get("qualified_name")]

    # Scan for related subgraphs (all type directories in flat output)
    for subdir in sorted(out_dir.iterdir()):
        if not subdir.is_dir() or subdir.name == file_type:
            continue
        # Load related subgraph for cross-edge detection
        nodes, edges = load_subgraph(subdir)
        if nodes:
            related_category = subdir.name
            subgraphs[related_category] = (nodes, edges)
            ui_data_by_subgraph[related_category] = load_elements(subdir)
            all_symbols.extend(_node_to_symbol(n) for n in nodes if _node_to_symbol(n).get("qualified_name"))

    print(f"  Loaded {len(subgraphs)} related subgraphs for {file_type}")

    # Merge elements from all subgraphs into one dict for cross-file plugins
    merged_elements: dict = {"templates": []}
    for sg_elements in ui_data_by_subgraph.values():
        for key, value in sg_elements.items():
            if key == "templates":
                merged_elements.setdefault(key, []).extend(value or [])
            else:
                merged_elements.setdefault(key, []).extend(value if isinstance(value, list) else [])

    # Produce cross-file edges
    cross_edges = produce_cross_file_edges(subgraphs, all_symbols, merged_elements, ast_data=None)

    # Deduplicate first (normalize from_qname/to_qname vs from_node/to_node)
    cross_edges = deduplicate_cross_file_edges(cross_edges)

    # Classify edges by layer
    for edge in cross_edges:
        from_layer = edge.get("from_layer", "")
        to_layer = edge.get("to_layer", "")
        edge["category"] = classify_edge_category(from_layer, to_layer)

    # Filter to only edges involving the target type
    target_cross_edges = [
        e for e in cross_edges
        if file_type in e.get("from_file", "") or file_type in e.get("to_file", "")
        or category in [e.get("from_layer"), e.get("to_layer")]
    ]

    print(f"  Generated {len(target_cross_edges)} cross-file edges for {file_type}")

    # Write output
    result = {
        "nodes": target_nodes,
        "edges": target_edges,
        "cross_file_edges": target_cross_edges,
        "metadata": {
            "file_type": file_type,
            "role": category,
            "total_nodes": len(target_nodes),
            "total_edges": len(target_edges),
            "related_cross_edges": len(target_cross_edges),
        },
    }

    cross_edges_file = out_dir / file_type / "cross_file_edges.json"
    cross_edges_file.parent.mkdir(parents=True, exist_ok=True)
    cross_edges_file.write_text(
        json.dumps(target_cross_edges, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  Written: {cross_edges_file}")

    return result
