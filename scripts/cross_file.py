def _node_to_symbol(node: dict) -> dict:
    """Convert a node dict (from nodes.jsonl) to a symbol dict with qualified_name.

    Nodes from _write_jsonl_from_graph: use 'id' = 'file::{path}' and 'label'
    Nodes from template/config handlers: use 'qualified_name'
    We preserve the full qname for cross-file lookups.

    lang field is preserved from the original node (from the language plugin).
    If absent, we derive it from the file extension.
    """
    qname = node.get("qualified_name") or node.get("id", "")
    start_val = node.get("start")
    line_num = start_val[0] if start_val else 1
    lang = node.get("lang", "")
    if not lang:
        lang = _infer_lang_from_file(node.get("file", ""))
    return {
        "qualified_name": qname,
        "name": node.get("label", ""),
        "kind": node.get("kind", "unknown"),
        "file": node.get("file", ""),
        "line": line_num,
        "lang": lang,
        "scope": node.get("scope", ""),
        "type_hint": node.get("type_hint"),
        "modifiers": node.get("modifiers", []),
        "annotations": node.get("annotations", []),
        "docstring": node.get("docstring"),
        "start": node.get("start", (1, 0)),
        "end": node.get("end", (1, 0)),
    }


def _infer_lang_from_file(file_path: str) -> str:
    """Derive language from file extension."""
    if not file_path:
        return ""
    import os
    _, ext = os.path.splitext(file_path.lower())
    mapping = {
        ".py": "python",
        ".js": "javascript",
        ".jsx": "javascript",
        ".ts": "javascript",
        ".tsx": "javascript",
        ".vue": "vue",
        ".java": "java",
        ".go": "go",
        ".rs": "rust",
        ".cpp": "cpp",
        ".c": "c",
        ".h": "c",
        ".hpp": "cpp",
        ".cs": "csharp",
        ".rb": "ruby",
        ".php": "php",
        ".swift": "swift",
        ".kt": "kotlin",
        ".scala": "scala",
        ".xml": "xml",
        ".html": "html",
        ".css": "css",
        ".scss": "css",
        ".json": "json",
        ".yaml": "yaml",
        ".yml": "yaml",
        ".md": "markdown",
        ".sql": "sql",
        ".sh": "shell",
        ".bash": "shell",
        ".ps1": "powershell",
    }
    return mapping.get(ext, "")


def _extract_file_from_qname(qname: str) -> str:
    """Extract file path from a qname that may have a prefix like template::, file::, config::.

    Examples:
        "template::/path/to/file.jsp" → "/path/to/file.jsp"
        "config::/path/to/web.xml" → "/path/to/web.xml"
        "template::/path/to/file.vue#66" → "/path/to/file.vue" (strips line suffix)
        "jp.co.rct.jj.action.JJ901ABTEST001Action" → "" (no prefix, no file)
    """
    if not qname:
        return ""

    # Strip line number suffix (#66) first
    if "#" in qname:
        qname = qname.split("#", 1)[0]

    # Check for known prefixes
    for prefix in ("template::", "file::", "config::", "url::", "tag::", "fn::", "ext::"):
        if qname.startswith(prefix):
            return qname[len(prefix):]

    return ""


def _infer_type_from_qname(qname: str) -> str:
    """Infer file type from a qname that may contain file path or class name.

    Examples:
        "/path/to/file.java" → "java"
        "jp.co.rct.jj.action.JJ901ABTEST001Action" → "java" (Java package dots)
        "template::/path/to/file.jsp" → "jsp"
        "src.server.chat.ChatResponse" → "python" (src/ prefix → Python module)
        "scripts.generate_test_report" → "python"
    """
    if not qname:
        return ""

    # Extract file path if there's a prefix
    file_path = _extract_file_from_qname(qname)

    # If we have a file path, get extension
    if file_path:
        import os
        _, ext = os.path.splitext(file_path)
        if ext:
            return ext.lstrip(".").lower()

    # Check for Python module qname patterns (src., scripts., tests., lib.)
    # These have dots but are NOT Java packages
    python_qname_prefixes = (
        "src.", "scripts.", "tests.", "test.", "lib.", "setup.",
        "tests.", "docs.", "examples.", "tools.", "apps.",
    )
    if "." in qname and qname[0].isalpha():
        # Check if it looks like a Python module path (starts with known Python dir prefixes)
        if any(qname.startswith(p) for p in python_qname_prefixes):
            return "python"
        # For other dotted names, assume Java (has package dots like jp.co., org., com.)
        return "java"

    return ""

