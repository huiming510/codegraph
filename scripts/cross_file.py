def _reconstruct_elements(nodes: list[dict]) -> dict:
    """Reconstruct template/config entry data from nodes.jsonl for cross-file plugins.

    Instead of a separate stage1b_ui.json, element data (JSP, TLD, dicon, etc.)
    is embedded directly in the node dicts. This function extracts it back into
    the dict-of-lists format that cross-file plugins expect.

    Node kinds that map to entries:
      - template_page, jsp_*         → "templates" / "jsp"
      - tld_tag                       → "tld"
      - web_xml_*                     → "xml" (web_xml)
      - dicon_component, dicon_include → "dicon"
      - servlet_def, servlet_mapping   → merged into "xml"
      - filter_def, filter_mapping     → merged into "xml"
    """
    result: dict = {"templates": []}

    # Group template-file nodes by their file
    template_files: dict[str, list] = {}
    tld_entries: list = []
    web_xml_entries: list = []
    dicon_entries: list = []
    result_map_entries: list = []

    for node in nodes:
        kind = node.get("kind", "")
        fp = node.get("file", "") or ""
        line = (node.get("start", [1, 0])[0] if node.get("start") else 1)
        attrs = node.get("attrs", {})

        # Template files and their elements
        # NOTE: Order matters! Check elements BEFORE template_page to avoid
        # jsp_custom_tag etc. being captured by the first condition but with empty elements
        if kind in ("jsp_expression", "jsp_form_field", "jsp_custom_tag",
                    "jsp_struts_action", "jsp_include", "jsp_directive"):
            tag_name = node.get("tag_name", "")
            if fp not in template_files:
                template_files[fp] = {
                    "file": fp,
                    "elements": [],
                    "parse_error": False,
                }
            template_files[fp]["elements"].append({
                "name": node.get("name", tag_name),
                "kind": kind,
                "line": line,
                "tag_name": tag_name,
                "attrs": attrs,
            })
        elif kind == "template_page":
            if fp not in template_files:
                template_files[fp] = {
                    "file": fp,
                    "elements": [],
                    "parse_error": False,
                }

        # TLD tags - need to track which TLD file they belong to
        elif kind == "tld_tag":
            # Extract TLD file from the node's file path (node.file is the TLD file)
            tld_file = fp
            # Use label as name since name field is often empty
            # Label format: "prefix:tagname" like "c_rt:param"
            tag_label = node.get("label", "")
            tag_name = node.get("name", tag_label)
            if not tag_name:
                tag_name = tag_label
            # Use class_name for compatibility with tld_handler plugin
            tld_entries.append({
                "name": tag_name,
                "class_name": attrs.get("class_name", ""),
                "line": line,
                "file": tld_file,
                "entry_type": "tld_tag",  # Required by _find_defined_tags
                "attrs": attrs,
            })

        # Web XML elements
        elif kind in ("servlet_def", "servlet_mapping", "filter_def",
                      "filter_mapping", "jsp_property_group", "config_file"):
            web_xml_entries.append({
                "entry_type": kind,
                "name": node.get("name", attrs.get("name", attrs.get("servlet_name", ""))),
                "class_name": attrs.get("class_name", attrs.get("servlet_class", "")),
                "line": line,
                "attrs": attrs,
            })

        # Dicon components
        elif kind in ("dicon_component", "dicon_include"):
            dicon_entries.append({
                "name": node.get("name", attrs.get("name", "")),
                "class_name": attrs.get("class_name", ""),
                "line": line,
            })

        # MyBatis XML Mapper result_map entries
        elif kind == "result_map":
            # class_name in attrs holds the Java entity type (from XML type="pkg.Entity")
            java_type = attrs.get("class_name", "")
            result_map_entries.append({
                "entry_type": "result_map",
                "name": node.get("name", ""),
                "java_type": java_type,
                "line": line,
                "attrs": attrs,
                "file": fp,
            })

    # Convert template_files dict → list
    result["templates"] = list(template_files.values())
    result["jsp"] = list(template_files.values())  # alias

    # Group tld_entries by their file
    tld_by_file: dict[str, list] = {}
    for tld_entry in tld_entries:
        tld_file = tld_entry.get("file", "")
        if tld_file not in tld_by_file:
            tld_by_file[tld_file] = []
        tld_by_file[tld_file].append(tld_entry)

    if tld_by_file:
        result["tld"] = [{"file": f, "entries": entries} for f, entries in tld_by_file.items()]
    if web_xml_entries:
        result["xml"] = [{"entries": web_xml_entries, "file": "", "config_type": "web_xml"}]
    if dicon_entries:
        result["dicon"] = [{"entries": dicon_entries, "file": ""}]
    if result_map_entries:
        result["result_maps"] = [{"entries": result_map_entries, "file": ""}]

    return result


def load_subgraph(subgraph_dir: Path) -> tuple[list[dict], list[dict]]:
    """Load nodes and edges from a subgraph directory.

    Returns:
        (nodes, edges) loaded from nodes.jsonl and edges.jsonl
    """
    nodes: list[dict] = []
    edges: list[dict] = []

    nodes_file = subgraph_dir / "nodes.jsonl"
    if nodes_file.exists():
        for line in nodes_file.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line:
                nodes.append(json.loads(line))

    edges_file = subgraph_dir / "edges.jsonl"
    if edges_file.exists():
        for line in edges_file.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line:
                edges.append(json.loads(line))

    return nodes, edges


def load_elements(subgraph_dir: Path) -> dict:
    """Load template/config element data from a subgraph directory.

    Priority:
    1. stage1b_ui.json (old pipeline format — contains parsed elements with attrs)
    2. Reconstruct from nodes.jsonl (new pipeline — elements embedded in nodes)

    Returns:
        Element dict with keys like "templates", "jsp", "tld", "dicon", etc.
    """
    ui_file = subgraph_dir / "stage1b_ui.json"
    if ui_file.exists():
        try:
            return json.loads(ui_file.read_text(encoding="utf-8"))
        except Exception:
            pass

    # Fallback: reconstruct from nodes
    nodes, _ = load_subgraph(subgraph_dir)
    return _reconstruct_elements(nodes)


def load_all_subgraphs(role_dir: Path) -> dict[str, tuple[list[dict], list[dict]]]:
    """Load all subgraphs from a role directory.

    Args:
        role_dir: Root output directory (e.g. output/01_source/backend/)

    Returns:
        Dict mapping subgraph_name -> (nodes, edges)
    """
    subgraphs: dict[str, tuple[list[dict], list[dict]]] = {}

    if not role_dir.exists():
        return subgraphs

    for subdir in role_dir.iterdir():
        if subdir.is_dir():
            nodes, edges = load_subgraph(subdir)
            if nodes or edges:
                subgraphs[subdir.name] = (nodes, edges)

    return subgraphs


