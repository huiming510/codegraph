# ── Registry for file-type handlers ──────────────────────────────────────

_SOURCE_LANGS = {"java", "python", "javascript", "go", "cpp"}
_TEMPLATE_LANGS = {"jsp", "jinja2", "react", "vue", "html_template"}
_FRAMEWORK_LANGS = {"tld", "struts_config", "web_xml", "dicon"}
_CONFIG_LANGS = {"yaml", "json", "properties", "env"}


def get_handler(type_name: str) -> Optional[object]:
    """Get the appropriate FileTypeHandler for a type name."""
    if type_name in _SOURCE_LANGS:
        return SourceCodeHandler(type_name)
    if type_name in _TEMPLATE_LANGS:
        return TemplateHandler(type_name)
    if type_name in _FRAMEWORK_LANGS:
        return FrameworkHandler(type_name)
    if type_name in _CONFIG_LANGS:
        return ConfigHandler(type_name)
    return None


# ══════════════════════════════════════════════════════════════════════════════
#  Unified run_for_type() — one function for all file types
# ══════════════════════════════════════════════════════════════════════════════

def run_for_type(src_root: Path, out_dir: Path, type_name: str,
                files: list[Path], do_full_graph: bool = False) -> SubgraphStats:
    """Unified subgraph builder for all file types.

    Three-stage pipeline (replaces the old 5-stage for source, stage1b for templates,
    and ad-hoc config parsing):

    Stage 1: Parse    → ParseEntry list (AST / elements / entries)
    Stage 2: Subgraph → nodes + intra-type edges
    Stage 3: Write    → nodes.jsonl + edges.jsonl

    Args:
        src_root: Source project root
        out_dir:  Output directory for this subgraph
        type_name: File type identifier (e.g. "java", "jsp", "yaml")
        files:    Pre-classified list of files to parse
        full_graph: If True, output all nodes/edges (no pruning)

    Returns:
        SubgraphStats with file counts and node/edge counts
    """
    handler = get_handler(type_name)
    if handler is None:
        print(f"  [WARN] No handler for type: {type_name}")
        return SubgraphStats()

    out_dir.mkdir(parents=True, exist_ok=True)

    # Stage 1: Parse (handler.parse shows tqdm progress internally)
    print(f"  [Stage 1] Parsing {type_name.upper()} in {src_root}...")
    entries = handler.parse(src_root, files)
    failed = sum(1 for e in entries if e.parse_error)
    ok_count = len(entries) - failed
    print(f"  [Stage 1]   {ok_count} OK, {failed} failed out of {len(entries)} files")

    # Stage 2: Build subgraph
    print(f"  [Stage 2] Building subgraph...")
    if type_name in _SOURCE_LANGS:
        # Source code: use SourceCodeHandler with AST parsing
        ast_data = [
            {"file": e.file, "ast": e.metadata.get("ast"), "parse_error": e.parse_error}
            for e in entries
        ]
        graph, stats = handler.build_subgraph(src_root, entries, full_graph=do_full_graph)
        print(f"  [Stage 2]   {stats.nodes} nodes, {stats.edges} edges")
        _print_intra_type_summary(graph.get("edges", []))

        # Stage 3: Write JSONL
        print(f"  [Stage 3] Writing subgraph...")
        # For source code, ui_data is not needed for template edge resolution
        handler.write_jsonl(graph=graph, ast_data=ast_data, ui_data={},
                          out_dir=out_dir, full_graph=do_full_graph)
    else:
        symbols, edges, stats = handler.build_subgraph(src_root, entries, full_graph=do_full_graph)
        print(f"  [Stage 2]   {stats.nodes} nodes, {stats.edges} edges")
        _print_intra_type_summary(edges)

        # Stage 3: Write JSONL
        print(f"  [Stage 3] Writing subgraph...")
        handler.write_jsonl(symbols=symbols, edges=edges, entries=entries,
                         out_dir=out_dir, full_graph=do_full_graph)

    # Build per-type viewer (optional, lightweight)
    _build_viewer(out_dir)

    return stats


def run(src_root: Path, out_dir: Path, lang: str = "java",
         full_graph: bool = False) -> dict:
    """Run the 5-stage pipeline for a single language.

    Args:
        src_root: Source code directory to analyze.
        out_dir: Output directory for graph files.
        lang: Language plugin to use. Default: "java".
              Valid: "java", "python", "javascript", "go", "cpp".
        full_graph: If True, output full graph (all tiers, all nodes).
                    If False (default), output filtered graph (CORE tier only,
                    no reachability pruning overhead).
    """
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"Stage 1: Syntax Parsing ({lang})...")
    ast_data = parse_directory(src_root, lang)
    (out_dir / "stage1_ast.json").write_text(
        json.dumps(ast_data, ensure_ascii=False, indent=2), encoding="utf-8")
    failed = [e for e in ast_data if e.get("parse_error")]
    print(f"  {len(ast_data)} files parsed, {len(failed)} failed")

    print(f"Stage 2: Symbol Table Building...")
    sym_data = build_symbol_table(ast_data, lang, str(src_root))
    (out_dir / "stage2_symbols.json").write_text(
        json.dumps(sym_data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  {len(sym_data['symbols'])} symbols")

    print(f"Stage 3: Type Inference...")
    typed_data = infer_types(sym_data)
    (out_dir / "stage3_typed.json").write_text(
        json.dumps(typed_data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  types resolved")

    # ── Parse templates using unified TemplateRegistry ───────────────────────────
    print(f"Stage 1b: UI Parsing ({lang} templates)...")
    ui_data = {}
    
    try:
        from plugins.tpl_02_template.web_pages import TemplateRegistry
    except ImportError:
        from .plugins.tpl_02_template.web_pages import TemplateRegistry
    
    template_plugins = TemplateRegistry.get_for_lang(lang)
    for plugin in template_plugins:
        for pattern in plugin.file_patterns:
            for fp in src_root.rglob(pattern):
                result = TemplateRegistry.parse_file(fp)
                if result is None:
                    continue
                d = result.to_dict()
                kind = plugin.legacy_result_key or plugin.name
                if kind not in ui_data:
                    ui_data[kind] = []
                ui_data[kind].append(d)
                if "templates" not in ui_data:
                    ui_data["templates"] = []
                ui_data["templates"].append(d)

    jsp_ok = sum(1 for e in ui_data.get("jsp", []) if not e.get("parse_error"))
    jinja2_ok = sum(1 for e in ui_data.get("jinja2", []) if not e.get("parse_error"))
    react_ok = sum(1 for e in ui_data.get("react", []) if not e.get("parse_error"))
    vue_ok = sum(1 for e in ui_data.get("vue", []) if not e.get("parse_error"))
    print(f"  {jsp_ok} JSP, {jinja2_ok} Jinja2, {react_ok} React, {vue_ok} Vue files")

    print(f"Stage 4: Reference Resolution...")
    refs_data = resolve_references(typed_data, ast_data, ui_data, lang,
                                   filter_quality=not full_graph)
    (out_dir / "stage4_refs.json").write_text(
        json.dumps(refs_data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  {len(refs_data['edges'])} edges")

    print(f"Stage 5: Semantic Enrichment...")
    graph = semantic_enrichment(refs_data, ui_data, lang, skip_prune=full_graph)
    (out_dir / "stage5_graph.json").write_text(
        json.dumps(graph, ensure_ascii=False, indent=2), encoding="utf-8")

    _write_jsonl_from_graph(graph, ast_data, ui_data, out_dir, full_graph=full_graph)
    _write_method_bodies(graph, ast_data, out_dir)

    # Count actual entry points and web_action nodes from graph
    entry_pts = graph.get("entry_points", [])
    web_action_count = sum(
        1 for s in graph.get("symbols", [])
        if s.get("control_flow") == "web_action"
    )
    stats = _compute_stats(out_dir, entry_points=len(entry_pts),
                           web_action_count=web_action_count)
    _build_viewer(out_dir)
    _print_summary(src_root, out_dir, ast_data, stats, lang, full_graph=full_graph)

    return graph


def _write_config_jsonl(graph: dict, out_dir: Path):
    """Write nodes.jsonl and edges.jsonl for a config subgraph."""
    nodes_path = out_dir / "nodes.jsonl"
    edges_path = out_dir / "edges.jsonl"

    with nodes_path.open("w", encoding="utf-8") as f:
        for sym in graph["symbols"]:
            node = {
                "id": sym["qualified_name"],
                "label": sym.get("name", ""),
                "kind": sym.get("kind", "config"),
                "file": sym.get("file", ""),
                "start": [sym.get("line", 1), 0] if sym.get("line") else None,
                "end": [sym.get("line", 1), 0] if sym.get("line") else None,
                "lang": sym.get("lang", ""),
            }
            if "attrs" in sym:
                node["attrs"] = sym["attrs"]
            f.write(json.dumps(node, ensure_ascii=False) + "\n")

    with edges_path.open("w", encoding="utf-8") as f:
        for edge in graph["edges"]:
            f.write(json.dumps(edge, ensure_ascii=False) + "\n")


def run_all(src_root: Path, out_dir: Path,
            full_graph: bool = False) -> dict:
    """New --all mode: classify files → unified subgraph pipeline → cross-file edges.

    Output structure:
        output/
        ├── 01_source/backend/
        │   └── nodes.jsonl, edges.jsonl   (all source types merged)
        ├── 02_template/web_pages/
        │   └── nodes.jsonl, edges.jsonl  (all template types merged)
        ├── 03_config/app_config/
        ├── 03_config/framework/java/
        ├── cross_file_edges.json
        └── all_graph.json

    Args:
        src_root: Source code directory to analyze.
        out_dir: Output directory for graph files.
        full_graph: If True, output full graph (all tiers, all nodes).

    Returns:
        Master graph dict
    """
    print(f"\n{'='*60}")
    print(f"Stage 0: Project File Classification")
    print(f"{'='*60}")

    # Stage 0: classify all project files into role architecture categories
    classification = classify_project_files(src_root)
    print_classification_summary(classification)

    # Build output structure (creates all directories, even empty ones)
    build_output_structure(out_dir)

    # ── Phase 1: Build subgraphs per (category, handler) ─────────────────────
    all_subgraph_dirs: list[Path] = []

    for category in sorted(classification.keys()):
        files = classification[category]
        if not files:
            continue

        layer, subcat = category.split("/", 1)
        category_out = out_dir / layer / subcat
        category_out.mkdir(parents=True, exist_ok=True)

        print(f"\n{'='*60}")
        print(f"Processing category: {layer}/{subcat}")
        print(f"  Total files: {len(files)}")
        print(f"{'='*60}")

        # Group files by which handler can process them
        type_groups = _group_files_by_handler(files)
        if not type_groups:
            skipped = [str(fp).replace(str(src_root), "").lstrip(os.sep) for fp in files]
            print(f"  No supported file types ({len(files)} skipped): {', '.join(skipped)}")
            continue

        type_list = ", ".join(sorted(type_groups.keys()))
        print(f"  Detected {len(type_groups)} type(s): {type_list}")

        for type_name in sorted(type_groups.keys()):
            type_out = category_out / type_name
            type_out.mkdir(parents=True, exist_ok=True)
            all_subgraph_dirs.append(type_out)

            print(f"\n--- Parsing {type_name.upper()} files ({len(type_groups[type_name])} files) ---")
            run_for_type(src_root, type_out, type_name,
                        type_groups[type_name], do_full_graph=full_graph)

    # ── Phase 2: Merge all subgraphs ──────────────────────────────────────────
    print(f"\n{'='*60}")
    print(f"Merging Subgraphs")
    print(f"{'='*60}")
    print(f"  Collecting from {len(all_subgraph_dirs)} subgraph(s)...")

    _all_nodes = []
    _all_edges = []
    for sub_dir in all_subgraph_dirs:
        nodes_file = sub_dir / "nodes.jsonl"
        edges_file = sub_dir / "edges.jsonl"
        if nodes_file.exists():
            _all_nodes.extend(_load_jsonl(nodes_file))
        if edges_file.exists():
            _all_edges.extend(_load_jsonl(edges_file))

    print(f"  Merged: {len(_all_nodes)} nodes, {len(_all_edges)} edges")

    if _all_nodes:
        (out_dir / "nodes.jsonl").write_text(
            "\n".join(json.dumps(n, ensure_ascii=False) for n in _all_nodes),
            encoding="utf-8")
        (out_dir / "edges.jsonl").write_text(
            "\n".join(json.dumps(e, ensure_ascii=False) for e in _all_edges),
            encoding="utf-8")
        print(f"  Written: nodes.jsonl, edges.jsonl")

    # Guard: _build_viewer requires both files to exist
    if not (out_dir / "nodes.jsonl").exists() or not (out_dir / "edges.jsonl").exists():
        print(f"  Error: nodes.jsonl or edges.jsonl missing — cannot build viewer")
        print(f"  This may indicate all nodes were pruned (no code reachable from any file type).")
        return {}

    # ── Phase 3: Stage 6 — cross-file edge generation ─────────────────────────
    print(f"\n{'='*60}")
    print(f"Stage 6: Cross-File Edge Generation")
    print(f"{'='*60}")

    total_graph = generate_cross_file_edges(src_root, out_dir)

    # Build viewer for the master graph
    print(f"\n{'='*60}")
    print(f"Building Master Graph Viewer")
    print(f"{'='*60}")
    _build_viewer(out_dir)

    # Print final summary
    meta = total_graph.get("metadata", {})
    print(f"""
===== --all Pipeline Complete =====
Source:    {src_root}
Output:    {out_dir}
Subgraphs: {len(all_subgraph_dirs)} (across {len(classification)} categories)
Total nodes: {len(_all_nodes)}
Total edges: {len(_all_edges)}
Cross-file edges:
  Intra-Type:   {meta.get('intra_type_count', 0)}  (Stage 4: language plugins)
  Cross-Type:   {meta.get('cross_type_count', 0)}  (Stage 6: CrossFilePlugins)
  Cross-Layer:  {meta.get('cross_layer_count', 0)}  (Stage 6: CrossFilePlugins)
Output files:
  {out_dir / 'cross_file_edges.json'}
  {out_dir / 'all_graph.json'}
  {out_dir / 'graph_viewer.html'}
""")
    return total_graph


def _match_pattern(filename: str, pattern: str) -> bool:
    """Simple glob pattern match: *.java, *.py, *.js, etc."""
    import fnmatch
    return fnmatch.fnmatch(filename, pattern)


def _group_files_by_handler(files: list[Path]) -> dict[str, list[Path]]:
    """Group files by which handler will process them.

    Tries each handler in order:
      1. LanguageRegistry (source code: java, python, javascript, go, cpp)
      2. TemplateRegistry (templates: jsp, jinja2, react, vue, ...)
      3. JavaConfigRegistry (Java framework configs: tld, struts, web_xml, dicon)
      4. ConfigRegistry (app configs: yaml, json, properties, env)

    Returns dict: {type_name: [path, ...]}

    Files are assigned to the FIRST handler that matches them.
    Handlers are checked in priority order to avoid overlap
    (e.g., JSP files should go to TemplateRegistry, not BuildRegistry).
    """
    groups: dict[str, list[Path]] = defaultdict(list)
    assigned: set[str] = set()  # file paths already assigned

    for fp in files:
        fp_str = str(fp)
        if fp_str in assigned:
            continue

        # 1. Source code (LanguageRegistry — backend)
        from plugins.src_01_source.backend import LanguageRegistry
        for plugin in LanguageRegistry.all_plugins():
            for pat in plugin.get_file_patterns():
                if _match_pattern(fp.name, pat):
                    groups[plugin.lang_id].append(fp)
                    assigned.add(fp_str)
                    break
            if fp_str in assigned:
                break
        if fp_str in assigned:
            continue

        # 1b. Source code (LanguageRegistry — frontend, e.g. javascript)
        try:
            from plugins.src_01_source.frontend import LanguageRegistry as FrontendRegistry
            for plugin in FrontendRegistry.all_plugins():
                for pat in plugin.get_file_patterns():
                    if _match_pattern(fp.name, pat):
                        groups[plugin.lang_id].append(fp)
                        assigned.add(fp_str)
                        break
                if fp_str in assigned:
                    break
        except ImportError:
            pass
        if fp_str in assigned:
            continue

        # 2. Template (TemplateRegistry)
        try:
            from plugins.tpl_02_template.web_pages import TemplateRegistry
        except ImportError:
            from .plugins.tpl_02_template.web_pages import TemplateRegistry
        plugin = TemplateRegistry.get_for_file(fp)
        if plugin:
            groups[plugin.name].append(fp)
            assigned.add(fp_str)
            continue

        # 3. Java framework config (JavaConfigRegistry)
        try:
            from plugins.cfg_03_config.framework.java import JavaConfigRegistry
        except ImportError:
            from .plugins.cfg_03_config.framework.java import JavaConfigRegistry
        plugin = JavaConfigRegistry.get_for_file(fp)
        if plugin:
            groups[plugin.name].append(fp)
            assigned.add(fp_str)
            continue

        # 4. App config (ConfigRegistry)
        try:
            from plugins.cfg_03_config.app_config import ConfigRegistry
        except ImportError:
            from .plugins.cfg_03_config.app_config import ConfigRegistry
        plugins = ConfigRegistry.get_for_file(fp)
        if plugins:
            groups[plugins[0].name].append(fp)
            assigned.add(fp_str)
            continue

    return groups


def _detect_lang_for_category(category: str) -> Optional[str]:
    """Detect the language plugin ID for a role architecture category."""
    if "/java" in category or "/backend" in category:
        return "java"
    if "/python" in category:
        return "python"
    if "/javascript" in category or "/frontend" in category or "/vue" in category:
        return "javascript"
    if "/go" in category:
        return "go"
    if "/cpp" in category or "/embedded" in category:
        return "cpp"
    return None


def run_singlefile(src_root: Path, out_dir: Path,
                   file_type: str, full_graph: bool = False) -> dict:
    """New --singlefile mode: parse only the specified type, output subgraph + related cross-file edges.

    Workflow:
    1. Stage 0: Get the role for the specified file type
    2. Unified pipeline: parse → subgraph → write
    3. Stage 6: Generate cross-file edges for this type
    4. Output subgraph + related cross-file edges (NOT the complete master graph)

    Args:
        src_root: Source code directory to analyze.
        out_dir: Output directory for graph files.
        file_type: File type code (e.g., "java", "jsp", "yaml")
        full_graph: If True, output full graph (all tiers, all nodes).

    Returns:
        Subgraph dict with nodes, edges, and related cross-file edges
    """
    from stage0_classify import get_role_for_file_type

    role = get_role_for_file_type(file_type)
    if role is None:
        print(f"Unknown file type: {file_type}")
        print(f"Available types: {', '.join(sorted(FILE_TYPE_CODE_MAP.keys()))}")
        return {}

    layer, subcat = role
    category = f"{layer}/{subcat}"

    print(f"\n{'='*60}")
    print(f"Stage 0: File Type '{file_type}' -> {layer}/{subcat}")
    print(f"{'='*60}")

    # Build output structure
    build_output_structure(out_dir)
    category_out = out_dir / layer / subcat
    category_out.mkdir(parents=True, exist_ok=True)

    # Group files for this type using the unified handler
    from stage0_classify import classify_project_files
    classification = classify_project_files(src_root)
    files = classification.get(category, [])

    if not files:
        print(f"No files found for type: {file_type}")
        return {}

    # Filter to only the files for this type
    type_groups = _group_files_by_handler(files)
    type_files = type_groups.get(file_type, [])

    if not type_files:
        print(f"No files matched type: {file_type}")
        return {}

    print(f"Found {len(type_files)} files for type: {file_type}")

    # Run unified pipeline
    type_out = category_out / file_type
    type_out.mkdir(parents=True, exist_ok=True)
    run_for_type(src_root, type_out, file_type, type_files, do_full_graph=full_graph)

    # Stage 6: Cross-file edges for this type only
    print(f"\n{'='*60}")
    print(f"Stage 6: Cross-File Edge Generation for {file_type}")
    print(f"{'='*60}")

    result = generate_cross_file_edges_for_type(src_root, out_dir, file_type)

    print(f"""
===== --singlefile Pipeline Complete =====
Type:     {file_type}
Role:     {layer}/{subcat}
Files:    {len(type_files)}
Output:   {type_out}
""")
    return result


def _load_jsonl(path: Path) -> list[dict]:
    """Load all records from a JSONL file."""
    results = []
    if path.exists():
        for line in path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line:
                results.append(json.loads(line))
    return results


def _compute_stats(out_dir: Path, entry_points: int = 0, web_action_count: int = 0) -> dict:
    nodes_path = out_dir / "nodes.jsonl"
    edges_path = out_dir / "edges.jsonl"

    node_kinds = {}
    edge_kinds = {}
    all_domain_tags: set[str] = set()
    all_control_flows: set[str] = set()
    total_nodes = 0
    total_edges = 0

    with nodes_path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            total_nodes += 1
            n = json.loads(line)
            k = n.get("kind", "unknown")
            node_kinds[k] = node_kinds.get(k, 0) + 1
            for tag in n.get("domain_tags") or []:
                all_domain_tags.add(tag)
            cf = n.get("control_flow")
            if cf:
                all_control_flows.add(cf)

    with edges_path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            total_edges += 1
            e = json.loads(line)
            k = e.get("kind", "unknown")
            edge_kinds[k] = edge_kinds.get(k, 0) + 1

    call_edges = edge_kinds.get("call", 0)

    return {
        "total_nodes": total_nodes,
        "total_edges": total_edges,
        "node_kinds": node_kinds,
        "edge_kinds": edge_kinds,
        "domain_tags": sorted(all_domain_tags),
        "control_flows": sorted(all_control_flows),
        "call_edges": call_edges,
        "entry_points": entry_points,
        "web_action_count": web_action_count,
    }


