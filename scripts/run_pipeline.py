# ══════════════════════════════════════════════════════════════════════════════
#  Unified File Type System
# ══════════════════════════════════════════════════════════════════════════════
#  Design principle: all file types (source code, templates, configs) are
#  first-class citizens. Each FileTypeHandler produces:
#    1. parse()        → raw parse results (AST / elements / entries)
#    2. build_subgraph() → nodes + intra-type edges
#    3. write_jsonl()    → nodes.jsonl + edges.jsonl
#  The three-stage subgraph pipeline is shared for ALL types.

FileType = Literal[
    "java", "python", "javascript", "go", "cpp",
    "jsp", "jinja2", "react", "vue", "go_template", "html_template",
    "yaml", "json", "properties", "env",
    "tld", "struts_config", "web_xml", "dicon",
]


@dataclass
class ParseEntry:
    """Unified parse result for any file type.

    This replaces:
    - AST dicts from stage1_syntax (for source code)
    - TemplateParseResult.to_dict() (for templates)
    - JavaConfigResult.to_dict() (for framework configs)
    - Dependency lists (for build files)
    - dict entries (for app configs)
    """
    file: str
    kind: FileType = "java"
    elements: list[dict] = field(default_factory=list)
    parse_error: bool = False
    error_msg: Optional[str] = None
    metadata: dict = field(default_factory=dict)

    def to_dict(self) -> dict:
        return {
            "file": self.file,
            "kind": self.kind,
            "elements": self.elements,
            "parse_error": self.parse_error,
            "error_msg": self.error_msg,
            "metadata": self.metadata,
        }

    @classmethod
    def from_ast(cls, entry: dict) -> "ParseEntry":
        """Convert a stage1_syntax AST entry to ParseEntry."""
        return cls(
            file=entry["file"],
            kind="java",
            elements=[],  # raw AST stored in stage JSON, not here
            parse_error=entry.get("parse_error", False),
            error_msg=entry.get("error_msg"),
            metadata={"ast": entry.get("ast")},
        )

    @classmethod
    def from_template(cls, result) -> "ParseEntry":
        """Convert a TemplateParseResult to ParseEntry."""
        d = result.to_dict() if hasattr(result, "to_dict") else result
        return cls(
            file=d["file"],
            kind=d.get("kind", "template"),
            elements=d.get("elements", []),
            parse_error=d.get("parse_error", False),
            error_msg=d.get("error_msg"),
            metadata={},
        )


def _print_intra_type_summary(edges: list) -> None:
    """Print a summary of Intra-Type edge kinds from the subgraph's edge list.

    Intra-Type edges are cross-file edges of the same type generated by language plugins
    in Stage 4: imports, extends, implements, inner_class, and the named variants
    (js_import, ts_import, py_import, java_extends, etc.).
    """
    if not edges:
        return
    from collections import Counter
    all_kinds = Counter(e.kind if hasattr(e, "kind") else e.get("kind", "") for e in edges)
    # Intra-Type kinds are cross-file structural references (not internal)
    # NOTE: inner_class is excluded because it represents a SAME-FILE relationship
    #       (nested class definition), not a cross-file relationship.
    intra_kinds = {
        "imports", "extends", "implements",
        "js_import", "ts_import", "py_import", "java_import", "go_import",
        "jsp_include", "cpp_include",
        "java_extends", "java_implements",
    }
    intra_total = sum(cnt for k, cnt in all_kinds.items() if k in intra_kinds)
    if intra_total > 0:
        parts = [f"{k}={cnt}" for k, cnt in sorted(all_kinds.items()) if k in intra_kinds]
        print(f"  [Stage 2]   Intra-Type edges: {intra_total}  ({', '.join(parts)})")


@dataclass
class SubgraphStats:
    """Statistics for a subgraph."""
    file_count: int = 0
    parse_failed: int = 0
    nodes: int = 0
    edges: int = 0
    elements: int = 0


